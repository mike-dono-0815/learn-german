def parse_entry_line(line):
    """
    Parse a headword line and return a dict with word metadata.
    Formats:
      verb [form1, form2, ...]
      der/die/das Noun, die Plural
      adjective/adverb/etc.
    """
    line = line.strip()

    article = None
    plural = None
    forms = []
    word = line

    # Extract verb forms from brackets: word [form1, form2]
    bracket_match = re.match(r'^(.+?)\s*\[(.+)\]\s*$', line)
    if bracket_match:
        word = bracket_match.group(1).strip()
        forms = [f.strip() for f in bracket_match.group(2).split(',')]

    # Extract article and plural: "der/die/das Noun, die Plural" or "der/die/das Noun, die Pl1 / die Pl2"
    noun_match = re.match(
        r'^(der|die|das)\s+([^,]+),\s*(.+)$', word, re.IGNORECASE
    )
    if noun_match:
        article = noun_match.group(1).strip()
        word = noun_match.group(2).strip()
        plural_raw = noun_match.group(3).strip()
        # Extract the plural form: "die Adressen" → "Adressen"
        plural_words = re.findall(r'die\s+(\S+)', plural_raw, re.IGNORECASE)
        if plural_words:
            plural = ' / '.join(plural_words)

    # Determine part of speech
    pos = guess_pos(line, word, article, forms)

    # Get base key for translation lookup
    base = get_base_key(word)
    translation = TRANSLATIONS.get(base) or TRANSLATIONS.get(word) or ""

    return {
        "word": word,
        "article": article,
        "plural": plural,
        "forms": forms,
        "type": pos,
        "translation_ru": translation,
    }


def get_base_key(word):
    """Return the canonical key to look up in TRANSLATIONS."""
    # Strip reflexive marker
    w = re.sub(r'\s*\(sich\)$', '', word).strip()
    # Strip parenthetical annotations like (Sg.), (bekommen / geben / sagen)
    w = re.sub(r'\s*\(.*\)$', '', w).strip()
    # Strip trailing -/-nen etc. if any
    w = re.sub(r',.*$', '', w).strip()
    return w


def guess_pos(raw_line, word, article, forms):
    if article:
        return "noun"
    if forms or re.search(r'\[', raw_line):
        return "verb"
    w_lower = word.lower()
    # Common non-verb, non-noun patterns
    particles = {'ab', 'an', 'auf', 'aus', 'bei', 'bis', 'durch', 'für', 'gegen',
                 'in', 'mit', 'nach', 'ohne', 'pro', 'seit', 'über', 'um', 'unter',
                 'von', 'vor', 'wegen', 'zu', 'zwischen', 'neben', 'hinter', 'vor',
                 'zwischen', 'her', 'hin', 'außer', 'gegenüber'}
    conjunctions = {'aber', 'als', 'also', 'dass', 'denn', 'deshalb', 'doch',
                    'obwohl', 'oder', 'sonst', 'und', 'weil', 'wenn', 'wie'}
    adverbs = {'auch', 'außerdem', 'außerhalb', 'bald', 'bar', 'besonders', 'bitte',
               'bisschen', 'da', 'dabei', 'damals', 'daneben', 'danke', 'dann',
               'dort', 'draußen', 'dringend', 'drinnen', 'drüben', 'endlich',
               'fast', 'ganz', 'gerade', 'geradeaus', 'gestern', 'gleich', 'genug',
               'hallo', 'heute', 'hier', 'hinten', 'hoffentlich', 'immer', 'ja',
               'jetzt', 'leider', 'links', 'manchmal', 'meistens', 'mehr',
               'mindestens', 'morgen', 'natürlich', 'nein', 'nie', 'nirgends',
               'noch', 'nur', 'oft', 'online', 'rechts', 'schon', 'sehr',
               'sofort', 'sogar', 'sonst', 'später', 'tschüs', 'überall',
               'übermorgen', 'unbedingt', 'unten', 'vielleicht', 'vorbei',
               'vorgestern', 'vorher', 'wahrscheinlich', 'wenigstens',
               'wieder', 'wirklich', 'zuerst', 'zuletzt', 'zurück', 'zusammen'}
    if w_lower in particles:
        return "preposition"
    if w_lower in conjunctions:
        return "conjunction"
    if w_lower in adverbs:
        return "adverb"
    # Words ending in -lich, -ig, -sam, -los often adjectives
    if re.search(r'(lich|ig|sam|los|isch|ell|ant|ent|iv|ös)$', w_lower):
        return "adjective"
    # Pronouns
    pronouns = {'alles', 'alle', 'allein', 'beide', 'einige', 'jeder', 'jede',
                'jedes', 'jemand', 'kein', 'keine', 'man', 'manch', 'nichts',
                'niemand', 'welcher', 'welche', 'welches', 'wer', 'wen', 'wem',
                'was', 'etwas'}
    if w_lower in pronouns:
        return "pronoun"
    # Default to adjective for uncategorized non-noun/verb
    return "word"


def is_sentence(line):
    """Heuristic: is this line a sentence (not a headword)?"""
    s = line.strip()
    if not s:
        return False
    # Section headers
    if s.startswith('#'):
        return False
    # If it contains a verb bracket or article+noun pattern it's a headword
    if re.match(r'^(der|die|das)\s+[A-ZÄÖÜ]', s):
        return False
    if re.match(r'^\w+\s*\[', s):
        return False
    # Starts with lowercase single word (headword entry like 'ab', 'aber')
    if re.match(r'^[a-zäöüß]\S*$', s):
        return False
    # Looks like a sentence: starts with capital and has typical sentence content
    return True


def looks_like_headword(line):
    """True if the line starts a new vocabulary entry."""
    s = line.strip()
    if not s:
        return False
    if s.startswith('#'):
        return False
    # Article + noun pattern
    if re.match(r'^(der|die|das)\s+[A-ZÄÖÜ]', s):
        return True
    # Verb with bracket forms
    if re.match(r'^\S.*\[', s):
        return True
    # Single lowercase word (preposition, conjunction, adverb, adjective)
    if re.match(r'^[a-zäöüß]\S*\s*$', s):
        return True
    # Single lowercase word followed by adjective forms
    if re.match(r'^[a-zäöüß]\S+\s', s) and not s[0].isupper():
        return True
    # Capitalized word (noun without article — rare, e.g. "Achtung (Sg.)")
    if re.match(r'^[A-ZÄÖÜ][a-zA-ZäöüÄÖÜß]', s) and not any(
        s.startswith(x) for x in ('Ich ', 'Er ', 'Sie ', 'Es ', 'Wir ', 'Du ',
                                   'Man ', 'Hast ', 'Hat ', 'Haben ', 'Kann ',
                                   'Kannst ', 'Können ', 'Bitte ', 'Hier ',
                                   'Da ', 'Das ', 'Die ', 'Der ')):
        return True
    return False


def split_sentences(text):
    """Split a line that contains multiple sentences into individual ones."""
    # Split on sentence-ending punctuation followed by space+capital
    parts = re.split(r'(?<=[.!?])\s+(?=[A-ZÄÖÜ])', text.strip())
    return [p.strip() for p in parts if p.strip()]


def main():
    with open(INPUT, 'r', encoding='utf-8') as f:
        content = f.read()

    # Find the ALPHABETISCHER WORTSCHATZ section
    match = re.search(r'# ALPHABETISCHER WORTSCHATZ', content)
    if not match:
        print("ERROR: Section not found")
        return

    section = content[match.start():]
    lines = section.split('\n')

    # Split into blocks separated by blank lines
    blocks = []
    current_block = []
    for line in lines:
        if line.strip() == '':
            if current_block:
                blocks.append(current_block)
                current_block = []
        else:
            current_block.append(line.strip())
    if current_block:
        blocks.append(current_block)

    words = []
    sentences = []
    assignments = []
    sentence_index = {}  # text → sentence id (dedup)
    word_id_counter = [0]
    sentence_id_counter = [0]

    def next_word_id():
        word_id_counter[0] += 1
        return f"w{word_id_counter[0]:04d}"

    def get_or_create_sentence(text):
        if text not in sentence_index:
            sentence_id_counter[0] += 1
            sid = f"s{sentence_id_counter[0]:04d}"
            sentence_index[text] = sid
            sentences.append({"id": sid, "text": text})
        return sentence_index[text]

    for block in blocks:
        if not block:
            continue
        first_line = block[0]

        # Skip section headers and intro line
        if first_line.startswith('#'):
            continue

        # The first line is the headword, remaining are sentences
        entry_meta = parse_entry_line(first_line)

        # Collect sentences from the rest of the block
        raw_sentences = []
        for line in block[1:]:
            if looks_like_headword(line) or line.startswith('#'):
                continue
            # Split multi-sentence lines
            parts = split_sentences(line)
            raw_sentences.extend(parts)

        # Filter to actual sentences (skip empty or headword-like)
        sentence_ids = []
        seen_in_entry = set()
        for s in raw_sentences:
            if not s or len(s) < 5:
                continue
            if s in seen_in_entry:
                continue
            seen_in_entry.add(s)
            sid = get_or_create_sentence(s)
            sentence_ids.append(sid)

        wid = next_word_id()
        entry_meta["id"] = wid
        words.append(entry_meta)

        if sentence_ids:
            assignments.append({
                "word_id": wid,
                "sentence_ids": sentence_ids
            })

    result = {
        "words": words,
        "sentences": sentences,
        "assignments": assignments
    }

    with open(OUTPUT, 'w', encoding='utf-8') as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    print(f"Done. {len(words)} words, {len(sentences)} sentences -> {OUTPUT}")


if __name__ == '__main__':
    main()
